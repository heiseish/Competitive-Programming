const int MOD = 1e8;
bool check(ll n) {
	n%=MOD;
	if (n <= MOD/2) return true;
	else return false;
}
long long H(long long previousHash, string &transaction,
            long long token) {
  long long v = previousHash;
  for (int i = 0; i < transaction.length(); i++) {
    v = (v * 31 + transaction[i]) % 1000000007;
  }
  return (v * 7 + token) % 1000000007;
}
