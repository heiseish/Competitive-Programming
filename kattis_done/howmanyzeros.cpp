ll count(long n) {
	if (n < 0) return 0L;
	ll result = 1L;
    ll i = 1;

    while (true) {
		ll b = n/i, c = n%i;
		ll a = b/10;
		b%=10;
        if (!a) 
			return result;
        if (!b)
            result += (a - 1) * i + c + 1;
        else
            result += a * i;
        i *= 10;
	}   
}

