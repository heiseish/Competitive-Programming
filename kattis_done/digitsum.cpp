int dp[10];
ll digits(long n) {
	ll ans = 0L;
	while(n>0) {
		ans+=(n%10);
		n/=10;
	}
	return ans;
}

ll solve(ll n) {
	if (n <= 0) return 0L;
	if (n < 10) {
		return dp[n];
	}
	int nd = (int)floor(1 + log10((long double) n)) - 1;
	ll mask = 1L * pow(10, (long double) nd);
	ll sumn = 1L * 45 * nd * mask / 10;
	ll omd = n / mask;
	sumn*=omd;
	ll ans = sumn + dp[omd - 1] * mask;
	ll num = n - (omd * mask) + 1;
	return ans + omd * num + solve(n%mask);
}
